
// ⭐ 8. Agentic AI vs Generative AI (Line-by-Line)
// ✔ Generative AI
// Generative AI is a type of artificial intelligence that creates new content.
// It can generate text, images, videos, music, code, and more.
// It works by predicting the most likely next token, pixel, or frame.
// Generative AI does not take actions on its own; it only creates output based on your prompt.
// It cannot plan, execute tasks, or use external tools unless you manually instruct it.
// ✔ Examples of Generative AI
// ChatGPT
// Google Gemini
// Claude
// Meta LLaMA
// Midjourney
// DALL·E
// ✔ Core Idea
// Generative AI = Creates things, but does not perform tasks autonomously.
// Example: If you ask it to write code, it writes the code but does not run, debug, or deploy it automatically.
// ✔ Agentic AI
// Agentic AI is an advanced AI system that can plan tasks, take actions, and use tools without constant user guidance.
// It can break a goal into subtasks and complete them step-by-step.
// It can interact with APIs, run code, access files, or automate workflows.
// Works more like an “AI worker” rather than just a chatbot.
// Includes decision-making abilities, memory, and multi-step reasoning.
// ✔ Examples of Agentic AI
// OpenAI Agents
// Devin (AI software engineer)
// AutoGPT
// LangChain Agents
// ReAct-based agents
// Microsoft AutoGen
// ✔ Core Idea
// Agentic AI = Acts autonomously like a digital assistant/employee.
// It can write code and also run it, fix errors, search the web, and deploy solutions.
// ⭐ One-Line Summary
// Generative AI generates content. Agentic AI performs tasks and takes actions autonomously.

// ⭐ 9. AI Tools (Meta AI, Gemini, Claude, Devin, etc.) — Line-by-Line Explanation
// ✔ Meta AI
// Developed by Meta (Facebook).
// Uses LLaMA models (open-source).
// Known for being lightweight, fast, and suitable for edge devices.
// Designed for mobile and real-time applications.
// Highly customizable by developers because it's open-source.
// ✔ Google Gemini
// Built by Google as a competitor to GPT-4.
// Extremely strong in reasoning, math, logic, and tool use.
// Integrated across Google products like Search, YouTube, Gmail, Chrome, and Workspace.
// Has multimodal abilities (text + image + video + audio).
// ✔ Claude (Anthropic)
// Known for its advanced writing & summarization skills.
// Extremely strong in long-context processing (up to millions of tokens).
// Great for legal documents, research papers, and large datasets.
// Prioritizes safety and correctness.
// ✔ Devin (AI Software Engineer)
// Devin is an Agentic AI built to perform real software engineering tasks.
// It writes code, runs code, fixes bugs, uses GitHub, and deploys projects.
// Works like an “AI programmer” that can execute multi-step tasks.
// ⭐ Final Summary (For Interviews)
// ⭐ Generative AI:
// Creates text, images, code, or content.
// Cannot take actions by itself.
// Example: ChatGPT, Gemini, Claude.
// ⭐ Agentic AI:
// Takes actions, uses tools, executes code, automates tasks.
// Works autonomously with planning and decision-making.
// Example: Devin, AutoGPT, OpenAI Agents.
// ⭐ AI Tools:
// Meta AI → Fast, lightweight, open-source (LLaMA).
// Gemini → Strong reasoning and multimodal intelligence.
// Claude → Best for long documents and advanced writing.
// Devin → AI that can act like a full software engineer.
