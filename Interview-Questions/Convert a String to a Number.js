
// ⭐ 73. How to Convert a String to a Number?
// ✔ 1. Number()
Number("10"); // 10

// ✔ 2. Unary +
+"10"; // 10

// ✔ 3. parseInt()
parseInt("10"); // 10

// ✔ 4. parseFloat()
parseFloat("10.5"); // 10.5

// ✔ 5. Using multiplication
"10" * 1; // 10

// ⭐ Interview Answer
// Use Number(), unary +, or parseInt/parseFloat to explicitly convert strings into numbers.