
// ⭐ 81. What is Dynamic Typing?
// JavaScript is a dynamically typed language, meaning:
// Variables do NOT have fixed types.
// The type is decided at runtime based on the value stored.
// A variable can change type anytime.

// ✔ Example:
let x = 10;     // number
x = "Akash";    // string
x = true;       // boolean

// No error → type changes based on assigned value.
// ⭐ Interview Answer
// Dynamic typing means variables are not bound to a type; their type is determined at runtime and can change anytime.