
// ⭐ 78. What is a Ternary Operator?
// A short form of if-else.

// ✔ Syntax:
// condition ? expression1 : expression2;

// ✔ Example:
let age = 18;
let result = age >= 18 ? "Adult" : "Minor";
console.log(result); // Adult

// ⭐ Interview Answer
// A ternary operator is a compact alternative to if-else, using ? :.