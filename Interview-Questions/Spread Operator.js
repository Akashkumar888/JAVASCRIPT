
// ⭐ 14. What is the Spread Operator ...
// Spread expands arrays or objects.

// ✔ Example:
const arr = [1, 2, 3];
const newArr = [...arr, 4];

// ✔ Object spread:
const obj = { name: "Akash" };
const newObj = { ...obj, age: 21 };

