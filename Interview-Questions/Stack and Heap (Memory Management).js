
// ⭐ 24. Difference Between Stack and Heap (Memory Management)
// JavaScript stores data in two memory areas:
// ✔ Stack
// Stores primitive values
// Stores function calls
// Last-In First-Out
// Fast access
// let a = 10;
// let b = "Hello";

// ✔ Heap
// Stores objects, arrays, functions
// Large, dynamic memory
// Slower access
// let obj = { name: "Akash" };
// ⭐ Interview Answer
// Primitive data → Stack
// Objects/Arrays → Heap